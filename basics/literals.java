public class literals {
    public static void main(String[] args) {
        // A literal is a value assigned directly to a variable in the program
        // if it is written directly in the code , it is a literal
        int a = 100;
        int b = 0b1010;   // binary
        int c = 012;      // octal
        int d = 0x1A;     // hexadecimal
        System.out.println(b+","+c+","+d);
    }
 }
